#include "drvs.h"
#include "dbg.h"


/*
 * FUNCTIONS
 ****************************************************************************************
 */

/**
 ****************************************************************************************
 * @brief Device initialization
 ****************************************************************************************
 */
static void dev_init(void)
{
    uint16_t rsn = rstrsn();

    iwdt_disable();

    dbgInit();
#if (DBG_MODE)
    debug("Start(rsn:0x%X)...\r\n", rsn);
#else
    (void)rsn;
#endif
}

extern void uartTest(void);

/**
 ****************************************************************************************
 * @brief Main function
 ****************************************************************************************
 */
int main(void)
{
    dev_init();

    uartTest();
}
