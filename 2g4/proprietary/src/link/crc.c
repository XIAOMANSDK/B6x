uint32_t crc24(uint8_t *data, uint16_t length)
{
    uint32_t crc = INIT_VALUE;
    while (length--)
    {
        crc = (crc >> 8) ^ crc_table[(crc & 0xFF) ^ *data++];
    }
    return crc;
}

void crc24_init()
{
    uint32_t crc;
    for (int i = 0; i < 256; i++)
    {
        crc        = 0;
        uint32_t t = i;

        for (int j = 0; j < 8; j++)
        {
            if (((crc ^ t) & 1) == 0)
            {
                crc = crc >> 1;
            }
            else
            {
                crc = POLYNOMIAL ^ (crc >> 1);
            }
            t >>= 1;
        }
        crc_table[i] = crc;
    }
}
